import { readdirSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath, pathToFileURL } from 'node:url'
import { getExtension } from '@bufbuild/protobuf'

// Builds src/well-known-events.generated.ts — the registry mapping every web
// well-known event name to its protobuf *PropertiesSchema. It introspects the schemas
// from a throwaway compile of src/gen produced by `make typed-events`
// (tsc -p tsconfig.gen.json -> .codegen-tmp). We import that compiled JS rather than
// the src/gen .ts source because node can't load protobuf-es's TS enums or its
// .js-extension relative imports directly. src/gen (vendored from proto/) is the single
// source of truth — nothing here reaches the Buf Schema Registry.

const __dirname = dirname(fileURLToPath(import.meta.url))
const repoRoot = join(__dirname, '..')

const COMPILED_DIR = join(repoRoot, '.codegen-tmp', 'gen', 'common', 'events', 'v1')
// Import path emitted into the generated file, relative to src/.
const OUTPUT_IMPORT_DIR = './gen/common/events/v1'

const importCompiled = mod => import(pathToFileURL(join(COMPILED_DIR, `${mod}_pb.js`)).href)

const { kind, Platform, platforms } = await importCompiled('options')

// Filter policy: include events whose `(common.events.v1.platforms)` option contains
// PLATFORM_WEB, OR is unset (platform-neutral). Events tagged only with non-WEB
// platforms (mobile/desktop/server) are excluded.
const SDK_PLATFORM = Platform.WEB
const isForThisPlatform = plats => !plats || plats.length === 0 || plats.includes(SDK_PLATFORM)

// Discover event modules from the compiled output. Any new *_events.proto vendored
// upstream is picked up automatically; options_pb is excluded by the suffix filter.
const EVENT_MODULES = readdirSync(COMPILED_DIR)
  .filter(f => f.endsWith('_events_pb.js'))
  .map(f => f.replace(/_pb\.js$/, ''))
  .sort()

const entries = []
for (const mod of EVENT_MODULES) {
  const m = await importCompiled(mod)
  for (const [name, schema] of Object.entries(m)) {
    if (!name.endsWith('PropertiesSchema')) continue
    const opts = schema?.proto?.options
    if (!opts) continue
    const k = getExtension(opts, kind)
    if (!k) continue
    if (!isForThisPlatform(getExtension(opts, platforms))) continue
    entries.push({ kind: k, module: mod, schema: name })
  }
}

entries.sort((a, b) => a.kind.localeCompare(b.kind))

const byModule = new Map()
for (const e of entries) {
  if (!byModule.has(e.module)) byModule.set(e.module, [])
  byModule.get(e.module).push(e.schema)
}

const importBlock = [...byModule.entries()]
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([mod, schemas]) => {
    const lines = [...schemas].sort().map(s => `  ${s},`)
    return `import {\n${lines.join('\n')}\n} from '${OUTPUT_IMPORT_DIR}/${mod}_pb.js'`
  })
  .join('\n')

const mapBlock = entries.map(e => `  ${e.kind}: ${e.schema},`).join('\n')

const fileContent = `// AUTOGENERATED — do not edit.
// Run \`make typed-events\` to regenerate (buf generate + this script).
// Source: buf.build/pugsh/pug protos, vendored under proto/ and generated into src/gen.
// Filter: events whose (common.events.v1.platforms) option contains PLATFORM_WEB or is unset.

/* eslint-disable */
${importBlock}

export const wellKnownSchemas = Object.freeze({
${mapBlock}
} as const)
`

const outPath = join(repoRoot, 'src', 'well-known-events.generated.ts')
writeFileSync(outPath, fileContent)
console.log(`Wrote ${entries.length} well-known events to ${outPath}`)
